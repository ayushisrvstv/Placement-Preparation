package beans;

public class ChangeM {
	String uname;
	String omobile;
	String mobile;

	
	public ChangeM(String uname, String omobile, String mobile) {
		super();
		this.uname = uname;
		this.omobile = omobile;
		this.mobile = mobile;
	}


	public String getUname() {
		return uname;
	}


	public void setUname(String uname) {
		this.uname = uname;
	}


	public String getOmobile() {
		return omobile;
	}


	public void setOmobile(String omobile) {
		this.omobile = omobile;
	}


	public String getMobile() {
		return mobile;
	}


	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	
	

}
